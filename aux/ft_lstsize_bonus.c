#include "libft.h"
#include "push_swap.h"
#include <stdio.h>
#include <stdlib.h>

int	ft_lstsize(t_node *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		i++;
		lst = lst->next;
	}
	return (i);
}
